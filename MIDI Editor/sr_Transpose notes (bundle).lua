-- @about
--    # Description
--    * This script bundle consists of scripts that transpose either all notes or selected notes.
--    * If you tranpose notes from the arrangement, selected notes are not taken into account, because you can't see what is selected from
--    the arrangement. If you want to tranpose from the inline editor, you MUST hover the mouse over the active inline editor, otherwise 
--    ALL notes will be affected, instead of only the selected ones.
--    * You can easily edit the scripts to adjust them to your needs or create new ones
--    * The scripts work in arrangement, MIDI editor and inline editor.
